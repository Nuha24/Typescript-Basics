function getArea(shape) {
    switch (shape.kind) {
        case "circle":
            return Math.PI * Math.pow(shape.radius, 2);
        case "square":
            return shape.side * shape.side;
        case "rectangle":
            return shape.length * shape.width;
        default:
            var exhaustiveCheck = shape;
            throw new Error("Unhandled case: ".concat(exhaustiveCheck));
    }
}
